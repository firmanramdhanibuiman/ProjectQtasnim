const numbers = [10, 20, 30, 40, 50];
const foundNumber = numbers.find(number => number > 25);
console.log(foundNumber);

// Metode find dalam JavaScript digunakan untuk mencari elemen pertama dalam array yang memenuhi kondisi yang ditentukan oleh fungsi pengujian. Fungsi pengujian ini akan diterapkan pada setiap elemen dalam array, dan metode .find() akan mengembalikan nilai elemen pertama yang memenuhi kondisi, atau undefined jika tidak ada elemen yang memenuhi kondisi.//

