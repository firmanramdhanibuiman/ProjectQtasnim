const numbers = [1, 2, 3, 4, 5];
const sum = numbers.reduce((accumulator, currentValue) => accumulator + currentValue, 1);
console.log(sum); 

// Metode reduce adalah metode pada objek Array di JavaScript yang digunakan untuk mereduksi array menjadi nilai tunggal melalui pengaplikasian suatu fungsi pada setiap elemen array. Fungsi ini mengambil empat argumen: akumulator, nilai saat ini, indeks saat ini, dan array itu sendiri.//
